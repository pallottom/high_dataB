class Anticoagulant(Base):
    """
    Reference table for anticoagulants used during blood collection.

    Attributes:
        id (int): Primary key.
        hash (str): PubChem SHA-256 hash (first 8 chars) for known substances.
                    Set to 'non available' for unknown anticoagulants.
        anticoagulant_name (str): Name of the anticoagulant. Required.

    Relationships:
        human_donors: One-to-many. Human donors collected with this anticoagulant.
        mouse_donors: One-to-many. Mouse donors collected with this anticoagulant.

    Constraints:
        Partial unique index on hash: hash must be unique only when it is NOT 'non available'.
        This allows multiple distinct unknown anticoagulants to coexist without collapsing.
    """

    __tablename__ = "anticoagulant"
    __table_args__ = (
        Index(
            "uq_anticoagulant_hash_known",
            "hash",
            unique=True,
            postgresql_where=text("hash <> 'non available'"),
        ),
    )
    id = Column(Integer, primary_key=True, autoincrement=True)
    hash = Column(String(64), nullable=False)
    anticoagulant_name = Column(String(100), nullable=False)

    human_donors = relationship("HumanDonor", back_populates="anticoagulant")
    mouse_donors = relationship("MouseDonor", back_populates="anticoagulant")


class CellType(Base):
    """
    Lookup table for cell types isolated from donors.

    Attributes:
        id (int): Primary key.
        name (str): Cell type name. Required and unique. Examples: 'PBMC', 'liver', 'neurons'.
    """

    __tablename__ = "cell_type"
    id = Column(Integer, primary_key=True, autoincrement=True)
    name = Column(String(50), nullable=False, unique=True)  # "PBMC", "liver", "neurons"


class CellCharacteristics(Base):
    """
    Lookup table for additional cell characterisation markers.

    Attributes:
        id (int): Primary key.
        name (str): Characteristic label. Required and unique. Examples: 'CD4+', 'CD8+'.
                    Maps to the DB column 'characteristic_name'.
    """

    __tablename__ = "cell_characteristics"
    id = Column(Integer, primary_key=True, autoincrement=True)
    name = Column("characteristic_name", String(100), nullable=False, unique=True)  # "CD4+", "CD8+", etc.
